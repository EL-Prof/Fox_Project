    private void searchFutureBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFutureBtnActionPerformed
       if(searchFutureCBox.getSelectedIndex() == 0)
       {
          if (!db.ValueIsExist(searchFutureTbl, 3 , searchFutureTxt.getText()))
          {
              search(searchFutureTxt);
          }
        else
          {
              JOptionPane.showMessageDialog(null, "تم البحث بالفعل");
          }
       }//end if
       
       else if(searchFutureCBox.getSelectedIndex() == 1)
       {
           Object[] row = new Object[5] ;
           query = "Select * from client " ;
           try 
        {
            rset = DB.stmt.executeQuery(query) ;
            if (!rset.next())
            {
                JOptionPane.showMessageDialog(null,"لا يوجد اى عميل");
            }
            else
            {
                do 
                {                    
                    row[4] = rset.getString(2) ;
                    row[3] = rset.getString(3) ;
                    row[2] = rset.getDouble(4) ;
                    row[1] = rset.getDouble(5) ;
                    row[0] =(double) row[2] - (double) row[1] ;
                
                    model = (DefaultTableModel) searchFutureTbl.getModel() ;
                    if (!db.ValueIsExist(searchFutureTbl, 3 , row[3].toString()))
                            {
                                model.addRow(row) ;
                            }
                    
                } while (rset.next());
                
                
                
            }
        } 
        catch (SQLException ex)
        {
            Logger.getLogger(SearchProduct.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(new JPanel(), "خطأ فى الاتصال بقاعدة البيانات.. تأكد من تشغيل السيرفر", "Error", JOptionPane.ERROR_MESSAGE);
        }
           
       }
       
    }//GEN-LAST:event_searchFutureBtnActionPerformed

