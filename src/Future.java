    private void search(JTextField text)
    {
        if(text.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"من فضلك ادخل رقم التليفون");
        }
        
        else
        {
            
        Object[] row = new Object[5] ;
        query = "Select * from client where mob_no = '" + text.getText() + "'" ;
        
        try 
        {
            rset = DB.stmt.executeQuery(query) ;
            if (!rset.next())
            {
                 JOptionPane.showMessageDialog(null, "هذا العميل غير مسجل");
                              
                           
            }
            
            
            else
            {
                do
                {
                  row[4] = rset.getString(2) ;
                  row[3] = rset.getString(3) ;
                  row[2] = rset.getDouble(4) ;
                  row[1] = rset.getDouble(5) ;
                  row[0] =(double) row[2] - (double) row[1] ;
                
                  model = (DefaultTableModel) searchFutureTbl.getModel() ;
                  model.addRow(row) ;
                } while(rset.next());
            }
        }
        
        catch (SQLException ex) 
        {
            Logger.getLogger(DB.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(new JPanel(), "خطأ فى الاتصال بقاعدة البيانات.. تأكد من تشغيل السيرفر", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
      }
        
    }

